<h1 class="titre_principal">Arthur Billaud</h1>

<article class="article">
    <p>
        L'inspiration de la nature est primordiale dans mes productions. <br/>
        Notre environnement nous procure toutes sortes de pressions captées par nos cinq sens qui nous permettent de produire notre propre expression, qui n'est qu'un condensé mélangé de nos ressentis et nos attraits. <br/>
        Je tente de pousser les limites du figuratif pour y découvrir subtilement un autre point de vue possible, et atteindre une présentation plus particulière que la représentation. <br/>
        Mon approche du graphisme est assez proche du dessin indiciaire.
    </p>
