    <p>
        Cette gravure est directement liée à la « Sonate des trilles du diable » (1713) et à l'histoire dont Giuseppe Tartini, le compositeur, l'entoure. L'anecdote de la sonate est sujette à de nombreuses illustrations postérieures, dont celle de Louis-Léopold Boilly (Le songe de Tarini - 1824), à qui le morceau est communément associé. <br/>
        Tartini raconte avoir fait un pacte avec le diable dans ses songes. En donnant son violon à l'être posé au pied de son lit, il est témoin d'une sonate immatérielle qui ne lui resta que partiellement en mémoire. De cette histoire, je ne veux garder que le côté fantasmagorique de l'être halluciné qu'est le diable, avec toute la mythologie chrétienne qui le compose au temps où le rêve est fait. C'est par jeu que je reprends l'esthétique déjà déformée empruntée par les chrétiens aux faunes romains et aux satyres grecs. En appuyant sur le caractère "mauvais" dont s'enveloppe cette chimère, démon mi-chèvre mi-humain, je la ramène à une catégorie du bestiaire immémorial auquel elle appartient. <br/>
        Le titre fait appel à une corde inexistante (le violon n'en a que quatre). Il est aussi un renvoi au violon traditionnel norvégien à 6 cordes, inventé par Isaac Nilssen Botnen (aujourd'hui appelé violon Hardanger).
    </p>
