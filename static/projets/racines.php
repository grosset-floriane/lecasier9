<article class="article">
    <p>Jai voulu dans un premier temps rendre hommage aux femmes de ma famille en
créant, tel un arbre généalogique, des branches sur lesquelles ne figurent que
les noms de femmes depuis de nombreuses générations. Alors que l'on dit souvent
que les femmes sont dans l'ombre des hommes, et elles l'ont souvent
véritablement été dans ma famille, j'ai trouvé intéressant de les faire
apparaître en retirant des morceaux de papier. Afin que le format tienne sur lui
même malgré les morceaux manquant, jai collé le tout sur du calque. Ensuite,
avec le travail de photo, j'ai pu mettre en lumière, au sens littéral, les noms en les éclairant par l'arrière de différentes façons.</p>
