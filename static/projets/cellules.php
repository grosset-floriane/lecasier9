    <p>Cette oeuvre nous montre une image de cellules mortes. 
Quand j'étais petit, par hasard, je suis allé à  l'hopital pour faire un examen ophtalmologique avec mes parents. Mais le docteur m'a dit que mon oeil droit avait un problème. Mon oeil droit auvait reçu un choc (peut être durant mon sommeil en me cognant au mur...). À  cause de cela je vois des cellules mortes.

C'est pourquoi, j'ai fait trois sérigraphies,à trois étapes, qui sont les grandes cellules mortes dans mon oeil droit.</p>
