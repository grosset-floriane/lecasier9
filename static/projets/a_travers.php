<article class="article">
    <p>Au fil de mes recherches, j'ai constaté que l'oignon, une fois décortiqué détient de réelles propriétés de transparence. J'ai donc souhaité exploiter ces qualités en créant des compositions translucides avec ceux-ci,
comme des vitraux. <br/>
Ici, les couleurs les plus significatives des               vitraux traditionnels ont été ajoutés à l'eau,              avec des tranches d'oignon et
des morceaux de peaux ( oignon rouge ). L'ensemble              est ensuite congelé afin
d'obtenir une réelle plaque. Illuminé, le morceau               de glace laisse passer la lumière et les qualités               de transparence de tous les éléments liés
doivent apparaître.</p>
