    <p>Ces expérimentations sont des papiers trempés dans l'eau, sur lesquels j'ai déposé d'autres papiers remplis de peinture épaisse. La peinture se propage alors toute seule. J'aime l'idée de laisser faire les choses, associer procédés techniques et hasard. À travers ces couleurs et leurs assemblages, je cherche ici à créer des formes proches de la nature (organiques ou minérales) ainsi qu'une sorte de galaxie mentale.</p>
