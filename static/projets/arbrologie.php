<article class="article">
    <p>
        Ce travail fait partie d'une période  où les questions que je me pose
        concernent le rapport, le lien, l'échange  entre l'homme et la nature. Il a
        débuté ici, par la figuration d'arbres en  aluminium, forme née
        instinctivement pendant mes recherches.     Petit à petit, l'arbre m'apparaît
        comme le symbole, l'essence  même de la vie naturelle, des origines de l'Homme.
        Ce matériau a été choisi pour ses propriétés esthétiques et physiques,
        mais aussi  pour ses connotations péjoratives dans mon esprit : industriel?
        nucléaire?  couverture de survie? Ce qui explique sûrement un titre qui
        ferait partie du  jargon médical.    Ces arbres ont été également
        présentés sous forme d'installation,  accompagnés d'un enregistrement
        sonore. Celui-ci était composé de bruits de  déchirement, d'écrasement et
        de tous les autres sons que peuvent faire les  mains au contact du papier
        aluminium ; et d'autres, plus organiques, comme le vent, la mer, le sol
        craquant sous des pieds ou le crépitement du feu par exemple.
    </p>
